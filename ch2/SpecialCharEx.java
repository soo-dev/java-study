package ch2;

class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\'');   // '
        System.out.println("abc\t123\b456");  // abc	12456
        System.out.println('\n');   // 줄바꿈
        System.out.println("\"hello\"");    // "Hello"
        System.out.println("c:\\"); // c:\
    }
}
